  static Map<String, String> getTranslations(String languageCode) {
    switch (languageCode) {
      case 'hi':
        return hi;
      case 'ta':
        return ta;
      case 'pa':
        return pa;
      case 'en':
      default:
        return en;
    }
  }

  static String translate(String key, String languageCode, {Map<String, String>? params}) {
    final translations = getTranslations(languageCode);
    String text = translations[key] ?? en[key] ?? key;
    
    if (params != null) {
      params.forEach((paramKey, value) {
        text = text.replaceAll('{$paramKey}', value);
      });
    }
    
    return text;
  }
